
class Demo11 {

    public static void main(String args[]) {
        int age = 50;
        switch (age) {
            case 1:
                System.out.println("Age is 10");
                break;
            case 2:
                System.out.println("Age is 15");
                break;
            case 3:
                System.out.println("Age is 20");
                break;
            case 4:
                System.out.println("Age is 25");
                break;
            default:
                System.out.println("No match");
        }
    }
}
